package com.example.skul5.domain;

public class loginObject {
	private String name;
	private String passwd;
	
	public loginObject() {
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	

}
