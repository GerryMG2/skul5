package com.example.skul5.domain;

public class Subject implements Model {

    private Integer id;

    private String name;

    public Subject() {
    }

    @Override
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
